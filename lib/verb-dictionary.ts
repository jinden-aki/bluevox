// ======================================================
// Utility: Lookup Maps
// ======================================================

export const ROOT_VERB_MAP = new Map(ROOT_VERBS.map((v) => [v.id, v]));
export const STRENGTH_VERB_MAP = new Map(STRENGTH_VERBS.map((v) => [v.id, v]));
export const WEAKNESS_VERB_MAP = new Map(WEAKNESS_VERBS.map((v) => [v.id, v]));

/** Get a root verb by ID */
export function getRootVerb(id: number): RootVerb | undefined {
  return ROOT_VERB_MAP.get(id);
}

/** Get a strength verb by ID (e.g. "S01") */
export function getStrengthVerb(id: string): StrengthVerb | undefined {
  return STRENGTH_VERB_MAP.get(id);
}

/** Get a weakness verb by ID (e.g. "W01") */
export function getWeaknessVerb(id: string): WeaknessVerb | undefined {
  return WEAKNESS_VERB_MAP.get(id);
}

/** Get the paired weakness verb for a strength verb */
export function getPairedWeakness(strengthId: string): WeaknessVerb | undefined {
  const sv = STRENGTH_VERB_MAP.get(strengthId);
  if (!sv) return undefined;
  return WEAKNESS_VERB_MAP.get(sv.paired_weakness);
}

/** Get the paired strength verb for a weakness verb */
export function getPairedStrength(weaknessId: string): StrengthVerb | undefined {
  const wv = WEAKNESS_VERB_MAP.get(weaknessId);
  if (!wv) return undefined;
  return STRENGTH_VERB_MAP.get(wv.paired_strength);
}

/** Get all root verbs by layer */
export function getRootVerbsByLayer(layer: string): RootVerb[] {
  return ROOT_VERBS.filter((v) => v.layer === layer);
}

/** Get all strength verbs that use a specific MSS axis */
export function getStrengthVerbsByAxis(axis: string): StrengthVerb[] {
  return STRENGTH_VERBS.filter((v) => v.mss_axes.includes(axis));
}
